					<h2>WHAT IS LIGHT POLLUTION?</h2>
					<h4>Based on Earth Law Center </h4>
					<BR>
					<p>Glare, light trespass, and skyglow are the three major forms of light pollution (in addition to over-illumination and clutter).</p>
					<p>Glare from unprotected lighting is a public-health risk, particularly as you get older. Glare light scattering in the eye causes a lack of contrast, can temporarily blind you, and can lead to dangerous driving conditions, for example.</p>
					<p>When unwanted light enters one's house, such as by projecting unwanted light into a sleeping person's bedroom window, it is known as light trespass.</p>
					<p>The term "skyglow" refers to the light that can be seen over densely populated areas. Skyglow is a mixture of reflected light and upward-directed (unshielded) light that escapes into the sky (and for the most part, unused).</p>
