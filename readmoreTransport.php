		<h2>Particles of Polluted Air Cause by Transportation</h2>
		<h3><strong>Carbon monoxide (CO)</h3></strong>
		<p>This odorless, colorless, and poisonous gas is formed by the combustion of fossil fuels such as 
		gasoline and is emitted primarily from cars and trucks. 
		When inhaled, CO blocks oxygen from the brain, heart, and other vital organs.</p> 
		
		<br>
		
		<h3><strong>Sulfur dioxide (SO2)</h3></strong>
		<p>Motor vehicles create this pollutant by burning sulfur-containing fuels, especially diesel and coal.
