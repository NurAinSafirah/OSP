					<h2>WHAT IS LAND POLLUTION?</h2>
					<h4>Based on Texas Disposal Systems </h4>
					<BR>
					<p>The degradation of the earth's land surfaces, both above and below ground level, is referred to as land pollution. The accumulation of solid and liquid waste materials contaminating groundwater and soil is the cause. Municipal solid waste (MSW) is the term used to describe these waste products, which contains both hazardous and non-hazardous waste.</p>
					<p>The permeability of the soil formations underneath the waste will increase or decrease the risk of land contamination when waste is deposited onto a piece of land. The greater the permeability of the soil, the greater the risk of land contamination. The risk of soil contamination has been greatly reduced by using the existing shale and clay in the land.</p>
					<p>Strong wastes were not collected with the climate in mind until the mid-twentieth century. Prior to that, waste was commonly dumped on the field in "wild dumps," which resulted in infestations of rodents, mosquitoes, and other diseases, as well as foul odours and windblown debris. Despite the fact that there are now substantially safer waste disposal methods, there are still a slew of other factors contributing to and exacerbating the issue.</p>
