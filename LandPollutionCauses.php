					<h2>CAUSES</h2
					<p> While there are many causes of land pollution, here are a few of the main contributors:  </p>
					<h4> Litter </h4>
					<p> Littering, or inappropriate waste disposal, is sadly all too normal. Any cigarette stub thrown on the ground or food wrapper thrown out a car window 
						contributes to a massive problem. Pedestrians and motorists are responsible for 76 percent of litter found on roadways, according to Keep America Beautiful. 
						However, not all litter is done on purpose. Unsecured objects that fall off the backs of vehicles or out of trash receptacles often contribute to a 
						significant amount of litter. Whether deliberate or not, all litter pollutes the environment by releasing chemicals and microparticles as it degrades.</p>
					<BR>
					<h4> Urbanization and Construction </h4>
					<p> Though urbanisation is not in and of itself a source of litter, large populations producing garbage and littering in a densely populated area eventually 
						results in land pollution. Construction activities are often carried out to support the increased population, resulting in vast amounts of waste materials 
						such as metal, plastic, wood, and bricks. When these materials aren't properly disposed of, they add to the area's land contamination. It's critical to 
						collaborate with partners who have integrated contractor strategies to achieve cost-effective construction recycling and waste management plans in order to 
						help reduce the environmental impact of construction sites.</p>
					<BR>
					<h4> Mining </h4>
					<p> Mining is the process of extracting minerals and other natural resources from the earth for a variety of purposes, including but not limited to making fuel 
						for cars, generating energy, and selling precious metals like gold and silver. However, this extraction and the techniques used deplete the earth's natural 
						resources while also causing harm and pollution. That is why discovering energy alternatives (such as solar and wind power) that aren't extracted from the 
						earth's surface is so important in helping to minimise land emissions in the future.</p>
					<BR>
					<h4> Agriculture </h4>
					<p> Agriculture is important to both daily life and the economy as a whole. It can, however, have far-reaching consequences for the world. Agricultural pollution 
						occurs when contamination is released into the atmosphere as a by-product of raising livestock and growing food crops, and the contamination is extensive.</p>
