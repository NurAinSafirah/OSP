				<div class="card-body">
					<h4 class="card-title text-center">Put on Noise-Cancelling Headphones and Earplugs</h4>
					<p class="card-text">Wearing earplugs is a cost-effective solution for reducing noise pollution. A pair of earplugs can reduce the amount of noise hitting your eardrums. Also, the noise-canceling headphones filter the unwanted noise well before it reaches the ears. So, it can be an effective remedy to deal with noise pollution in the short term.</p>
				</div>
			</div>
		</div>

		<div class="col-md-4">
