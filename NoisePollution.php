					<h4 class="card-title text-center">Plant Trees</h4>
					<p class="card-text">Planting trees and bushes around your property to reduce noise levels as trees have been established to be effective in reducing noise levels, which can be particularly helpful if you live in an urban area near busy roads. Therefore, numerous plants and trees in an area mean less noise pollution. Also, planting trees can help to improve the surrounding air quality.</p>
