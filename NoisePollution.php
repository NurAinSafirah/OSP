				<div class="card-body">
					<h4 class="card-title text-center">Turn Off Electronic Devices</h4>
					<p class="card-text">When the electrical devices and appliances are not in use, they can still produce some noise, even when they may be barely audible. At other times, however, it can be quite high-pitched. The solution is to turn them off when not in use and keep them on moderate volume to reduce noise pollution. Besides, you will also be saving electricity costs.</p>
				</div>
			</div>
		</div>

