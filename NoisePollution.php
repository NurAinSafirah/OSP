<div class="explain text-center">
	<h1 class="explain-title">Effect on Human</h1>
	<hr>
	<p class="explain-text" id="human">Exposure to excessive noise for a long time will cause many health problems such as stress, poor concentration, fatigue, hearing loss, hypertension, and even more severe issues such as cardiovascular and psychological dysfunction.</p>
</div>


<div class="explain text-center">
	<h1 id="wildlife" class="explain-title">Effect on Wildlife</h1>
	<hr>
	<p class="explain-text">Not only human will expose to the negative effects of noise pollution but also affects wildlife. It can interfere with animal communication as animals use soundwaves to communicate with each other. Thus, it will cause them to spend more time finding food and even abandon their territory. As the animal predators are hard to find food, the prey animals also more vulnerable to their predators.</p>
</div>

<div class="explain text-center">
	<h1 id="marine" class="explain-title">Effect on Marine Life</h1>
	<hr>
	<p class="explain-text">Similar to wildlife, marine life also use soundwaves to survive and communicate with each other. It will significantly impact the marine environment as noise can blanket a vast area that can interfere with the ability of marine life to hear natural sounds in the ocean. Thus, higher noise levels can reduce the ocean life's ability to hear environmental cues that are vital for survival, including those key to avoiding predators, finding food, and navigating to their preferred habitats.</p>
</div>

<div class="explain text-center">
	<h1 id="economy" class="explain-title">Effect on Social Economy</h1>
	<hr>
	<p class="explain-text">It is not only deadly for life but expensive too. It is because a variety of health problems and issues can be associated with noise pollution. As such, a significant negative impact on the economy can be observed from it. We know that noise pollution brings adverse effects on productivity via a combination of distraction, fatigue, and interruption of communication on humans. It will lead to the loss of productivity amounts for the country.</p>
</div>

