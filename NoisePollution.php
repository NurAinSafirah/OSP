			<p>Noise pollution is an invisible danger. It cannot be seen, but it is present nonetheless, both on land and under the sea. Noise pollution is considered to be any unwanted or disturbing sound that affects the health and well-being of humans and other organisms.</p>
			<br>
			<img src="https://miro.medium.com/max/1520/0*VZc7WxGgvkf9F8xf.png" class="center">
			<br>
			<p>Sound is measured in decibels. There are many sounds in the environment, from rustling leaves and whispers (20 to 30 decibels) to a thunderclap (120 decibels) to the rock concert and wail of a siren (120 to 140 decibels). Sounds that reach 85 decibels or higher are harmful to the environment. Sound sources that exceed this threshold include familiar things, such as the subway trains and shouted conversation (90 decibels), the sound of motorcycles (96 to 100 decibels), and chainsaw's sound (110 to 120 decibels).</p>
