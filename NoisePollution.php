					<h4 class="card-title text-center">Upgrade Your Insulation</h4>
					<p class="card-text">Install soundproofing materials on the ceiling and walls, such as acoustic wall panels. You also can put rugs and carpet on the floor as they can absorb the sound of hard floors. Carpets and mats can reduce the sound of someone walking by up to 34 decibels and reduce the reverberation time by half compared to hard flooring.</p>
