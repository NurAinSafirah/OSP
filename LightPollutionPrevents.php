					<h2>PREVENTIONS</h2>
					<p> Given the disastrous effects of land pollution, taking preventive measures to reduce its impact moving forward is crucial. </p>
					<div class = "container" style ="background-color: #966F33">
						<BR>
						<h3> Dark Sky Reserves </h3>
						<p> An IDA International Dark Sky Reserve is a public or private land area that is expressly protected for its science, ecological, educational, cultural, heritage, and/or public enjoyment because of its exceptional or distinguished standard of starry nights and nocturnal atmosphere. A reserve consists of a central region that meets minimum requirements for sky quality and natural darkness, as well as a periphery that aids in the core's dark sky protection.</p>
						<p> The Central Idaho Dark Sky Reserve is a 1,416-square-mile (3,670-square-kilometer) dark-sky preserve in Idaho, near the Sawtooth National Recreation Area. It was named as the first gold-tier dark sky preserve in the United States on December 18, 2017. </p>
						<p> To become a Dark Sky Preserve, the city had to remove visible artificial lighting while also implementing light pollution education programmes for the general public and surrounding towns. Outside the preserve's perimeter, the sky glow had to approximate that of a natural sky glow. Today, 97 percent of the park is classified as a wilderness area, free of light pollution, with year-round stargazing sites accessible via roads and trails. </p>
						<BR>
					</div>
					<div class = "container" style ="background-color: #FFFFFF">
						<BR>
						<h3> Governments Act </h3>
						<p> Between 2014 and 2016, the US Department of Energy's Outdoor Lighting Accelerator (OLA) collaborated with 25 partners (three states, 16 counties, and six regional energy networks) to upgrade 1.3 million street lights, saving cities an estimated $48 million per year.</p>
						<p> Across the pond, the European Union introduced new road lighting guidelines based on the ALARA concept (As Low As Reasonably Achievable). The basic guidelines, which aim to minimise energy consumption and light pollution, are in line with the Low Impact Lighting (LIL) norm, which has been promoted by German, Italian, and Slovenian members of the European Environmental Bureau in recent years.</p>
						<p> Many cities have taken the lead in redesigning their lighting systems in order to reclaim the night and reduce energy waste. The International Dark-Sky Association named Flagstaff, Arizona, as the first Dark Sky Community, and Chicago is in the process of retrofitting its fixtures.</p>
						<p> So far, research hasn't backed reports that dimmer, sparser lighting would lead to a rise in crime or reduced safety at night; in Chicago's West Garfield Park, more brightly lit alleys actually contributed to an increase in recorded crime. And, anecdotally, people are reporting that less blinding lighting makes it easier to see in dimly lit places, owing to our eyes' faster adaptation to darkness. </p>
						<BR>
