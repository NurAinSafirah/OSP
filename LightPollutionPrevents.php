						<h3> What Cities Can Do? </h3>
						<p> The concept of motion-sensitive street lighting is self-explanatory, and it has become a reality on this Barcelona street. When people and moving objects approach, lights turn on, lighting an environment ahead of time and minimising unnecessary light and energy. </p>
						<p> Light is directed downwards, where it is required, by street lighting that is encased above and to the sides. This decreases the amount of wasted light. This is known as "cutting off light at the horizontal," and it means that light is directed toward the ground rather than the sky.</p>
						<p> Switching to low-watt bulbs is a simple way to cut down on light emissions. In an urban scale, moving away from bluish-white lighting may be a bigger contributor to reduced light pollution. It's a win-win situation when LED lighting is replaced with warm-white lighting. VÃ©lez-MÃ¡laga, a Spanish market city (seems the Spanish are big on lighting innovation), recently introduced a dimmable warm-white light system for its streets, allowing for lower watt lighting on demand but also better illuminating its historic streets. </p>
