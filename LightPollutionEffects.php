					<h2>EFFECTS</h2>
					<p> <a href="https://sites.psu.edu/antoncivicissues/category/light-pollution/" target="_blank" style = "color: #4CD7F32">Many animals are affected by light pollution</a>
						because light and dark signals when to feed, sleep, hunt, migrate, or reproduce. As a result, light pollution alters and disrupts the timing of essential biological activities. Artificial light at night disrupts nocturnal pollination networks, which has a negative impact on plant reproduction. In one study, nocturnal visits to plants were decreased by 62 percent when artificially illuminated plantâ€“pollinator populations were compared to dark areas. </p>
					<p> Could light pollution be a factor in coral reef deterioration?<a href="https://www.nytimes.com/interactive/2016/04/07/science/light-pollution-effects-environment.html" target="_blank" style = "color: #4CD7F32"> By moonlight, more than 130 separate coral species on the Great Barrier Reef spawn new life. </a> 
						The reefs spew sperm and eggs into the ocean in what looks like an underwater blizzard every October or November after the full moon. Fertilization starts when the two sex cells merge amid the flurry. According to Oren Levy of Bar-Ilan University in Israel, bright city lights will obscure the moon's phases, throwing the corals' biological clocks out of whack. Reefs can release their reproductive cells late or not at all as a result of this, reducing their chances of producing offspring.</p>
					<p> Since most songbirds migrate at night, light pollution may throw off their circadian rhythms and cause them to become disoriented throughout migration. Large buildings with bright lights at night attract birds in the same way as bright porch lights attract moths, resulting in fatal collisions. </p>
