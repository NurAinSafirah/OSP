								the hatchlings migrate away from the dark silhouettes of the sand dunes and toward the lighter horizon of the ocean. However, as many coastal areas become more populated, artificial lights attract baby sea turtles away from the protection of the ocean and towards shore, where they risk dehydration, encountering predators, or being hit by vehicles. Any reduction in artificial lighting near beaches will aid in the protection of newly hatched sea turtles. </p>
