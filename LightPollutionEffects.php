					<p> Plants' life cycles are often affected by light pollution, which can prevent them from flowering and reproducing. This has an effect on the pollinators that pollinate these plants, as well as their own life cycles. Humans are also affected. One-third of human food needs pollination, according to the  
						<a href="https://www.nationalacademies.org/our-work/status-of-pollinators-monitoring-and-prevention-of-their-decline-in-north-america" target="_blank" style = "color: #4CD7F32"> National Academies Press Booklet "Resources on Pollinators." </a> </p>
					<p> <a href="https://cescos.fau.edu/observatory/lightpol-Amphib.html" target="_blank" style = "color: #4CD7F32">Amphibians are an important component </a>
						of forest and aquatic ecosystems, and their sensitivity to environmental changes makes them an important indicator species for ecosystem health. However, environmental stresses have caused their populations to decline around the world, and light pollution may play a significant role in this decline by altering nocturnal amphibians' foraging and breeding habits. </p>
					<p> Humans are also affected by light pollution. <a href="https://www.nps.gov/articles/nocturnal_earthnight.htm" target="_blank" style = "color: #4CD7F32">Humans, too, need natural darkness </a>
						to get a good night's sleep and stay healthy. By suppressing melatonin and can cortisol, a brain chemical produced when we are activated or excited, cool blue white light from sources such as LEDs, fluorescent, and metal halide lights interferes with the body's circadian rhythms at night. High cortisol levels make falling asleep and getting deep sleep difficult, which our immune systems need to combat illness and disease. </p>
