<!--Definition-->
<div class="explain text-center">
	<h1 class="display-4">What is Global Warming?</h1>
	<hr class="my-4">
	<p class="text" id="definition">Global warming is a term used for the observed century-scale rise in the Earth's climate system's average temperature and its related effects. The average temperature of the Earth's atmosphere is slowly increased because the amount of the energy (heat) striking the Earth from the sun is being trapped in the atmosphere and not radiated out into space.</p>
	<p class="text">Ironically, the best evidence of this may come from a terrible cooling event that took place some 1,500 years ago. One year after another, two massive volcanic eruptions placed so much black dust into the upper atmosphere that little sunlight could penetrate. Temperatures plummeted. Crops failed. People died of starvation, and the Black Death started its march. As the dust slowly fell to earth, the sun was again able to warn the world, and life returned to normal.</p>
	<p class="text">Today, the problem is not that too little sun warmth is reaching the Earth, but that too much is trapped in our atmosphere. So much heat is being kept inside the greenhouse earth that the temperature of the Earth is going up faster than at any previous time in history.</p>
</div>

<!--Cause-->
<div class="explain text-center">
	<h1 id="cause" class="display-4" style="background-color: #ffae41">What is The Cause?</h1>
	<hr class="my-4">
	<p class="text">Global warming occurs when carbon dioxide (CO2) and other air pollutants collect in the atmosphere and absorb sunlight and solar radiation that have bounced off the earth’s surface. Usually, this radiation would escape into space. Still, these pollutants, which can last for years to centuries in the atmosphere, trap the heat and cause the planet to get hotter. These heat-trapping pollutants, such as carbon dioxide, methane, nitrous oxide, water vapor, and synthetic fluorinated gases, are known as greenhouse gases. Their impact is called the <a href="https://en.wikipedia.org/wiki/Greenhouse_effect"> greenhouse effect.</a></p>
	<p class="text">Though natural cycles and fluctuations have caused the earth’s climate to change several times over the last 800,000 years, our current era of global warming is directly attributable to human activity. For example, nowadays, we used a lot of burning fossil fuels such as coal, oil, gasoline, and natural gas, which results in the greenhouse effect. In the United States, the largest greenhouse gas source is transportation which is 29 percent, followed by electricity production, 28 percent, and industrial activity with 22 percent.</p>
</div>


